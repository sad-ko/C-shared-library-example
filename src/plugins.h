#define create_string(str, size) { \
	str = (char*) calloc(size + 1, sizeof(char)); \
	if(!str) return NULL; \
}
/* ----------------------------------------------------------------------------------- */
#define calculate_string_size(str_to_save, size, ...) { \
	size = snprintf(NULL, 0, str_to_save, ##__VA_ARGS__); \
}
